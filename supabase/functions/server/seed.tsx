export async function seedData() {
  const seeded = await kv.get("data_seeded");
  if (seeded) {
    return false;
  }

  // Seed products
  const productKeys = products.map((p) => `product:${p.id}`);
  await kv.mset(productKeys, products);

  // Seed categories
  const catKeys = categories.map((c) => `category:${c.id}`);
  await kv.mset(catKeys, categories);

  // Seed messages
  const msgKeys = defaultMessages.map((m) => `message:${m.id}`);
  await kv.mset(msgKeys, defaultMessages);

  // Seed settings
  await kv.set("settings", defaultSettings);

  // Mark as seeded
  await kv.set("data_seeded", true);

  return true;
}
